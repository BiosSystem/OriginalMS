package client;

public enum MapleJob {
    BEGINNER(0),
    WARRIOR(100),
    FIGHTER(110),
    CRUSADER(111),
    HERO(112),
    PAGE(120),
    WHITEKNIGHT(121),
    PALADIN(122),
    SPEARMAN(130),
    DRAGONKNIGHT(131),
    DARKKNIGHT(132),
    MAGICIAN(200),
    FP_WIZARD(210),
    FP_MAGE(211),
    FP_ARCHMAGE(212),
    IL_WIZARD(220),
    IL_MAGE(221),
    IL_ARCHMAGE(222),
    CLERIC(230),
    PRIEST(231),
    BISHOP(232),
    BOWMAN(300),
    HUNTER(310),
    RANGER(311),
    BOWMASTER(312),
    CROSSBOWMAN(320),
    SNIPER(321),
    CROSSBOWMASTER(322),
    THIEF(400),
    ASSASSIN(410),
    HERMIT(411),
    NIGHTLORD(412),
    BANDIT(420),
    CHIEFBANDIT(421),
    SHADOWER(422),
    PIRATE(500),
    BRAWLER(510),
    MARAUDER(511),
    BUCCANEER(512),
    GUNSLINGER(520),
    OUTLAW(521),
    CORSAIR(522),
    NOBLESSE(1000),
    DAWN_WARRIOR(1100),
    DAWN_WARRIOR_2(1110),
    DAWN_WARRIOR_3(1111),
    BLAZE_WIZARD(1200),
    BLAZE_WIZARD_2(1210),
    BLAZE_WIZARD_3(1211),
    WIND_ARCHER(1300),
    WIND_ARCHER_2(1310),
    WIND_ARCHER_3(1311),
    NIGHT_WALKER(1400),
    NIGHT_WALKER_2(1410),
    NIGHT_WALKER_3(1411),
    THUNDER_BREAKER(1500),
    THUNDER_BREAKER_2(1510),
    THUNDER_BREAKER_3(1511),
    LEGEND(2000),
    ARAN_1(2100),
    ARAN_2(2110),
    ARAN_3(2111),
    ARAN_4(2112),
    GM(900),
    SUPERGM(910);

    final int jobid;

    private MapleJob(int id) {
        jobid = id;
    }

    public int getId() {
        return jobid;
    }

    public static MapleJob getById(int id) {
        for (MapleJob l : MapleJob.values()) {
            if (l.getId() == id) {
                return l;
            }
        }
        return null;
    }

    public static MapleJob getBy5ByteEncoding(int encoded) {
        switch (encoded) {
            case 2:
                return WARRIOR;
            case 4:
                return MAGICIAN;
            case 8:
                return BOWMAN;
            case 16:
                return THIEF;
            case 32:
                return PIRATE;
            case 1024:
                return NOBLESSE;
            case 2048:
                return LEGEND;
            default:
                return BEGINNER;
        }
    }

    public boolean isA(MapleJob basejob) {
        return getId() >= basejob.getId() && getId() / 100 == basejob.getId() / 100;
    }

    public boolean isCygnus() {
        return jobid >= 1000 && jobid <= 1511;
    }

    public boolean isAran() {
        return jobid >= 2000 && jobid <= 2112;
    }
}
